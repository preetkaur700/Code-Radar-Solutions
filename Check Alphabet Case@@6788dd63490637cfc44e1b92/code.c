#include <stdio.h>
int main()
{
    char character;
    scanf("%c",&character);
    if (character == a,b,c,d,e,f,g,h,i,j,k,l,m,n,o,p,q,r,s,t,u,v,w,x,y,z){
        printf("Lowercase");
    }else if(character == A,B,C,D,E,F,G,H,I,JK,L,M,N,O,P,Q,R,S,T,U,V,W,X,Y,Z){
        printf("Uppercase");
    }
    else{
        printf("Not an Alphabet");
    }
    return 0;
}

